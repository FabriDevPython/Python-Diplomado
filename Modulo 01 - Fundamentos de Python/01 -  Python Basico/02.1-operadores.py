# Operadores de comparacion
print(5 == 5) # Igualdad

print(2 != 4) # Desigualdad

print(5 < 3) # Menor que

print(5 <= 3) # Menor o igual que

print(5 > 3) # Mayor que

print(5 >= 3) # Mayor o igual que

