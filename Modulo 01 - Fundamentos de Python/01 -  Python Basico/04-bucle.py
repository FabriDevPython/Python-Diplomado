"""
Un bucle es una estructura de control de flujo que permite
repetir un bloque de código un número determinado o indeterminado de veces.
permiten ejecutar una tarea concreta varias veces, sin tener que escribir
manualmente el mismo código repetidamente.

Terminología de bucles:

Iteración o iterar: Es el proceso de recorrer objetos o elementos de una colección.

Iterador: Es un objeto que produce valores sucesivos a partir de un objeto iterable.

Iterable: Es un objeto sobre el que podemos iterar. Por ejemplo,
una lista, tupla, secuencia de números con range(), etc.

Variable iteradora: Es una variable que se utiliza para iterar
el objeto iterable. Normalmente, se llama i.

Expresión del bucle: Es el conjunto que representa las condiciones 
en las que se debe iterar el bucle. Por ejemplo, i < 5 es una expresión
que indica que el bucle se ejecute mientras que el valor de la variable
iteradora sea menor que 5. Como toda expresión, genera un resultado booleano:
evaluará como True mientras se cumpla y como False cuando ya no se cumpla.

Break: “Rotura” antes de tiempo de la ejecución del bucle.

Continue: Omisión de ciertas iteraciones del bucle.

Bucle: Es la estructura de control completa, que maneja todos estos elementos.
Ejemplos de bucle son for y while.

Secuencia: Se refiere a una serie de pasos que se llevan a cabo para completar
una tarea. Por ejemplo, [0, 1, 2, 3, 4] es una secuencia.
Ahí tendríamos 5 pasos en la secuencia.
"""